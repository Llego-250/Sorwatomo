<p>The Great Lakes region is one of the most fertile agricultural zones in Africa. It is also one of the most climate-vulnerable. At Sorwatom, sustainability is not a brand value we apply retrospectively to existing practice — it is a constraint we build into every sourcing and processing decision we make.</p>

<h2>3,200+ Partner Farms</h2>
<p>We work with over 3,200 smallholder farmers across Rwanda, Burundi and the DRC. These are not contract farmers in the industrial sense — they are independent producers who choose to grow to our quality standard because we offer a guaranteed offtake price that does not fluctuate with the spot market.</p>

<p>This price stability is itself a sustainability mechanism. A farmer who knows what their tomatoes will earn can invest in soil health, water management and crop rotation instead of maximising yield at the cost of the land.</p>

<h2>Zero-Waste Processing</h2>
<p>Our Kigali processing line produces two outputs: finished paste and tomato pomace — the skins and seeds remaining after extraction. Both are used. The pomace is processed into animal feed and distributed to local livestock farmers. Nothing goes to landfill.</p>

<p>Water used in washing and blanching is treated on-site before release. We are currently investing in a closed-loop water recovery system that will reduce freshwater consumption by an estimated 40% by 2027.</p>

<h2>Four-Layer Film Sachets</h2>
<p>Our signature four-layer flexible sachet — the only one of its kind manufactured south of the Sahara — uses 70% less material than an equivalent glass jar while providing superior protection against light and oxygen degradation. The paste inside lasts longer, wastes less, and gets to the consumer in better condition.</p>

<p>We are working with packaging engineers to introduce a partially bio-based film layer in 2027, reducing fossil-derived content in our packaging by approximately 35%.</p>

