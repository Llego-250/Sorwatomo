<p>Pilau is the celebratory rice of East Africa. Cooked for weddings, family gatherings, and special occasions, it gets its soul from a blend of whole spices that — when done right — fills the entire house with an aroma that signals something special is happening.</p>

<p>Our Pilau Masala blend was developed specifically for this dish: garlic, cumin, turmeric, cinnamon, black pepper and cardamom, ground in the proportions that East African cooks have used for generations.</p>

<h2>Ingredients (Serves 6)</h2>
<ul>
  <li>500g long-grain rice, washed and soaked for 30 minutes</li>
  <li>700g beef or chicken, cut into pieces</li>
  <li>1 sachet Sorwatom Pilau Masala (50g)</li>
  <li>2 large onions, finely sliced</li>
  <li>4 cloves garlic, minced</li>
  <li>1 tbsp fresh ginger, grated</li>
  <li>3 medium tomatoes, diced (or 1 tbsp Sorwatom Tomato Paste)</li>
  <li>3 tbsp cooking oil</li>
  <li>1 litre warm water or stock</li>
  <li>Salt to taste</li>
  <li>Fresh coriander and lime to serve</li>
</ul>

<h2>Method</h2>
<p><strong>Step 1 — Marinate the meat:</strong> Rub the meat with half the Pilau Masala, salt, garlic and ginger. Leave for at least one hour, ideally overnight.</p>

<p><strong>Step 2 — Brown the onions:</strong> Heat oil in a heavy-bottomed pot over medium heat. Fry onions until deep golden-brown — this is the base of the flavour and should not be rushed. At least 15–20 minutes.</p>

<p><strong>Step 3 — Add the meat:</strong> Add the marinated meat and brown on all sides. Add tomatoes (or tomato paste) and the remaining masala. Cook until the tomatoes break down and the oil begins to separate — about 10 minutes.</p>

<p><strong>Step 4 — Add rice and water:</strong> Drain the soaked rice and add to the pot. Stir to coat in the spiced oil. Pour in the warm stock or water, bring to a boil, then reduce to the lowest heat. Cover tightly and cook for 20 minutes without lifting the lid.</p>

<p><strong>Step 5 — Rest and serve:</strong> Remove from heat and rest, still covered, for 10 minutes. Fluff with a fork and serve with fresh coriander, sliced lime and kachumbari salad.</p>

<blockquote>
  <p>The difference between good pilau and unforgettable pilau is patience at the onion stage. Don\'t rush it.</p>
